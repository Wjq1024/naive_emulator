# 将寄存器 r1 初始化为 0，用于存储累加的结果
li r1, 0

# 将寄存器 r2 初始化为 10，表示我们要累加到的上限值 N = 10
li r2, 10

# 将寄存器 r3 初始化为 0，用作计数器，从 1 开始递增
li r3, 0

loop:  # 定义循环的起始标签
    # 将寄存器 r3 的值加 1，相当于让计数器递增 1
    addi r3, r3, 1

    # 将寄存器 r3 的值加到寄存器 r1 中，实现累加操作
    add r1, r1, r3

    # 如果寄存器 r3 的值不等于寄存器 r2 的值，则跳转回 "loop" 标签处继续执行循环
    bne r3, r2, loop

# 循环结束后，程序停止运行，这里将寄存器 r0 设置为 0（通常表示程序结束或空操作）
li r0, 0
