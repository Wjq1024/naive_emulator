# 累乘循环，计算出从 1 到 5 的所有整数的乘积（即阶乘 5!），
# 并将结果存储在寄存器 r1 中。最终结果为 120。


# 将寄存器 r1 初始化为 1，表示初始值 R(1) = 1
li r1, 1

# 将寄存器 r2 初始化为 6，r3 = 6 时，循环已经退出，就能计算5!

li r2, 6

# 将寄存器 r3 初始化为 1，用作计数器，从 1 开始递增
li r3, 1

loop:  # 定义循环的起始标签
    # 将寄存器 r1 的值与寄存器 r3 的值相乘，并将结果存回寄存器 r1
    # 相当于 R(1) = R(1) * R(3)
    mul r1, r1, r3

    # 将寄存器 r3 的值加 1，相当于让计数器递增 1
    addi r3, r3, 1

    # 如果寄存器 r3 的值不等于寄存器 r2 的值，则跳转回 "loop" 标签处继续执行循环
    # 这是控制循环次数的关键指令
    bne r3, r2, loop

# 循环结束后，程序停止运行，这里将寄存器 r0 设置为 0（通常表示程序结束或空操作）
li r0, 0
